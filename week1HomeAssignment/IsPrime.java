public class IsPrime 
{

	public static void main(String[] args) 
	{
		int n = 13;
		for (int i=13;i<=13; i++) 
		
			{
		
			if (i% n==0) 
			{
				System.out.println(i + " is a prime");
				
			}
			
			else 
			{
				System.out.println(i+ " is not a prime number");
			}
	        }
	}
}
	